function processDirectory(dir) {
    if (!fs.existsSync(dir)) return;
    const items = fs.readdirSync(dir);
    for (const item of items) {
        const fullPath = path.join(dir, item);
        if (fs.statSync(fullPath).isDirectory()) {
            processDirectory(fullPath);
        } else if (fullPath.endsWith('.js')) {
            let content = fs.readFileSync(fullPath, 'utf8');
            let modified = false;
            
            for (const [oldEmoji, newEmoji] of Object.entries(replaceMap)) {
                // escape some chars if needed, but String.replace with target is fine if done globally
                if (content.includes(oldEmoji)) {
                    // split and join is an easy way to replace all occurrences
                    content = content.split(oldEmoji).join(newEmoji);
                    modified = true;
                }
            }

            if (modified) {
                fs.writeFileSync(fullPath, content, 'utf8');
                console.log(`Updated ${fullPath}`);
            }
        }
    }
}

['./commands', './events', './utils', './archives'].forEach(processDirectory);
